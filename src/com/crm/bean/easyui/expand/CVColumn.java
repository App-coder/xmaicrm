package com.crm.bean.easyui.expand;

import com.crm.bean.easyui.Column;

public class CVColumn extends Column {
	
	private String fieldtabname;
	private String fieldcolname;
	private String fieldname;
	private String entitytype;
	private String fieldlabel;
	private String fieldtypeofdata;
	
	public String getFieldtabname() {
		return fieldtabname;
	}
	public void setFieldtabname(String fieldtabname) {
		this.fieldtabname = fieldtabname;
	}
	public String getFieldcolname() {
		return fieldcolname;
	}
	public void setFieldcolname(String fieldcolname) {
		this.fieldcolname = fieldcolname;
	}
	public String getEntitytype() {
		return entitytype;
	}
	public void setEntitytype(String entitytype) {
		this.entitytype = entitytype;
	}
	public String getFieldlabel() {
		return fieldlabel;
	}
	public void setFieldlabel(String fieldlabel) {
		this.fieldlabel = fieldlabel;
	}
	public String getFieldtypeofdata() {
		return fieldtypeofdata;
	}
	public void setFieldtypeofdata(String fieldtypeofdata) {
		this.fieldtypeofdata = fieldtypeofdata;
	}
	public String getFieldname() {
		return fieldname;
	}
	public void setFieldname(String fieldname) {
		this.fieldname = fieldname;
	}
	
}
