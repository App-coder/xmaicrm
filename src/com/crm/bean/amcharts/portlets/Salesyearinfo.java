package com.crm.bean.amcharts.portlets;

public class Salesyearinfo {
	
	private String year;
	private String sumgathersactual;
	private String sumexpensestotal;
	private String salessumtotle;
	private String salescount;
	private String newcustomer;
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getSumgathersactual() {
		return sumgathersactual;
	}
	public void setSumgathersactual(String sumgathersactual) {
		this.sumgathersactual = sumgathersactual;
	}
	public String getSumexpensestotal() {
		return sumexpensestotal;
	}
	public void setSumexpensestotal(String sumexpensestotal) {
		this.sumexpensestotal = sumexpensestotal;
	}
	public String getSalessumtotle() {
		return salessumtotle;
	}
	public void setSalessumtotle(String salessumtotle) {
		this.salessumtotle = salessumtotle;
	}
	public String getSalescount() {
		return salescount;
	}
	public void setSalescount(String salescount) {
		this.salescount = salescount;
	}
	public String getNewcustomer() {
		return newcustomer;
	}
	public void setNewcustomer(String newcustomer) {
		this.newcustomer = newcustomer;
	}
	
	
	
}
